import Bagbutik_Core
import Foundation

public struct AppCustomProductPageLocalization: Identifiable {
    public let id: String
    public var links: ResourceLinks?
    public var type: String { "appCustomProductPageLocalizations" }
    public var attributes: Attributes?
    public var relationships: Relationships?

    public init(id: String,
                links: ResourceLinks? = nil,
                attributes: Attributes? = nil,
                relationships: Relationships? = nil)
    {
        self.id = id
        self.links = links
        self.attributes = attributes
        self.relationships = relationships
    }

    public struct Attributes {
        public var locale: String?
        public var promotionalText: String?

        public init(locale: String? = nil,
                    promotionalText: String? = nil)
        {
            self.locale = locale
            self.promotionalText = promotionalText
        }
    }

    public struct Relationships {
        public var appCustomProductPageVersion: AppCustomProductPageVersion?
        public var appPreviewSets: AppPreviewSets?
        public var appScreenshotSets: AppScreenshotSets?

        public init(appCustomProductPageVersion: AppCustomProductPageVersion? = nil,
                    appPreviewSets: AppPreviewSets? = nil,
                    appScreenshotSets: AppScreenshotSets? = nil)
        {
            self.appCustomProductPageVersion = appCustomProductPageVersion
            self.appPreviewSets = appPreviewSets
            self.appScreenshotSets = appScreenshotSets
        }

        public struct AppCustomProductPageVersion {
            @NullCodable public var data: Data?
            public var links: Links?

            public init(data: Data? = nil,
                        links: Links? = nil)
            {
                self.data = data
                self.links = links
            }

            public struct Data: Identifiable {
                public let id: String
                public var type: String { "appCustomProductPageVersions" }

                public init(id: String) {
                    self.id = id
                }
            }

            public struct Links {
                public var related: String?
                public var itself: String?

                public init(related: String? = nil,
                            self itself: String? = nil)
                {
                    self.related = related
                    self.itself = itself
                }
            }
        }

        public struct AppPreviewSets {
            @NullCodable public var data: [Data]?
            public var links: Links?
            public var meta: PagingInformation?

            public init(data: [Data]? = nil,
                        links: Links? = nil,
                        meta: PagingInformation? = nil)
            {
                self.data = data
                self.links = links
                self.meta = meta
            }

            public struct Data: Identifiable {
                public let id: String
                public var type: String { "appPreviewSets" }

                public init(id: String) {
                    self.id = id
                }
            }

            public struct Links {
                public var related: String?
                public var itself: String?

                public init(related: String? = nil,
                            self itself: String? = nil)
                {
                    self.related = related
                    self.itself = itself
                }
            }
        }

        public struct AppScreenshotSets {
            @NullCodable public var data: [Data]?
            public var links: Links?
            public var meta: PagingInformation?

            public init(data: [Data]? = nil,
                        links: Links? = nil,
                        meta: PagingInformation? = nil)
            {
                self.data = data
                self.links = links
                self.meta = meta
            }

            public struct Data: Identifiable {
                public let id: String
                public var type: String { "appScreenshotSets" }

                public init(id: String) {
                    self.id = id
                }
            }

            public struct Links {
                public var related: String?
                public var itself: String?

                public init(related: String? = nil,
                            self itself: String? = nil)
                {
                    self.related = related
                    self.itself = itself
                }
            }
        }
    }
}
